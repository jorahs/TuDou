package com.luwei.domain;

public class ScrollSelect {
	int raw;
	int max;
	
	
	public ScrollSelect(int raw, int max) {
		super();
		this.raw = raw;
		this.max = max;
	}
	public int getRaw() {
		return raw;
	}
	public void setRaw(int raw) {
		this.raw = raw;
	}
	public int getMax() {
		return max;
	}
	public void setMax(int max) {
		this.max = max;
	}
	
}
